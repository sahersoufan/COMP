package AST.Elements.ElementsNodes.Miscallenous;

public class FunctionParameters {
    private String paraName;
    private Double paraNumber;
    private String paraString;
    private Boolean paraBoolean;

    public String getParaName() {
        return paraName;
    }

    public void setParaName(String paraName) {
        this.paraName = paraName;
    }

    public Double getParaNumber() {
        return paraNumber;
    }

    public void setParaNumber(Double paraNumber) {
        this.paraNumber = paraNumber;
    }

    public String getParaString() {
        return paraString;
    }

    public void setParaString(String paraString) {
        this.paraString = paraString;
    }

    public Boolean getParaBoolean() {
        return paraBoolean;
    }

    public void setParaBoolean(Boolean paraBoolean) {
        this.paraBoolean = paraBoolean;
    }
}
